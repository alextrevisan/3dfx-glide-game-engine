struct Triangle
{
    Triangle(Vector3 a, Vector3 b, Vector3 c)
    :vertex{a,b,c}{}
    std::array<Vector3, 3> vertex;
    Vector3& operator[] (size_t index)
    {
        return vertex[index];
    }
};

